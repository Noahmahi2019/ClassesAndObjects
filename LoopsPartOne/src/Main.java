public class Main {
    public static void main(String[] args) {
         // FOR LOOP
        for (int i = 1; i <= 10; i++) {
            {
                System.out.println(i);
            }

            // WHILE LOOP
            i = 1;
            while (i <= 10) {
                System.out.println(i);
                i++;
            }
            // DO WHILE LOOP
        }
        int i = 1;
        do {
            System.out.println(i);
            i++;
        }
        while (i <= 10);

    }
}
